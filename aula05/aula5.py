"""
+ -> Adição
- -> Subtração
* -> Multiplicação
/ -> Divisão
// -> Divisão Inteira
** -> Exponenciação
% -> Módulo (Resto da divisão)
() -> Pr
"""

print("Adição - ", 10 + 10)
print("Subtração - ", 10 - 10)
print("Multiplicação - ", 10 * 10)
print("Divisão - ", 10 / 10)
print("Divisão Inteira - ", 11 // 10)
print("Exponenciação - ", 10 ** 10);
print("Módulo - ", 10/10)


print("Sem () - ", 10 + 10 * 10)
print("Com () - ", (10 + 10) * 10)
